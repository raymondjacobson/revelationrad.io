	puts artist
	puts title

	x = title
	while x.gsub!(/\([^()]*\)/,""); end
	puts x
	titleSearch=x.strip()


	request="https://itunes.apple.com/search?"

	
	termLink="term="+artist.gsub(' ','+')+"+"+titleSearch.gsub(' ','+')+"&"
	limitLink="limit=1&"
	entityLink="entity=song"



	request+=termLink
	request+=limitLink
	request+=entityLink



	file = open(request)

	result=JSON.parse(file.read)["results"][0]


	album=result["collectionName"]
	puts album

	album_art=result['artworkUrl100']
	puts album_art

	track_download_url=result['trackViewUrl']
	puts track_download_url

	return_vals = {"artists" => artist,
	               "release_image" => album_art,
	               "title" => title,
	               "album" => album,
	               "track_download_url" =>track_download_url
	              }


	return return_vals

end

	def get_unique_name(id, streams)
		r = Random.new
		nine_digit = r.rand(0..1000000000).to_s
		unique = false
		streams_set = Set.new
		streams.each do |stream|
			streams_set.add(stream.id)
		end
		while !unique do
			found = false
			if streams_set.include?(nine_digit)
				found = true
			end
			if found==false
				unique = true
			end
		end
		first_three = remapped_numbers[nine_digit[0..2].to_i]
		second_three = remapped_numbers[nine_digit[3..5].to_i]
		third_three = remapped_numbers[nine_digit[6..8].to_i]
